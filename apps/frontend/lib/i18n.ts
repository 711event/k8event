export type FeKey = keyof (typeof feTranslations)["zh"];

/** Look up a frontend translation key, with optional interpolation */
export function tFe(
  locale: FeLocale,
  key: FeKey,
  vars?: Record<string, string | number>,
): string {
  const str = (feTranslations[locale] as Record<string, string>)[key] ?? key;
  if (!vars) return str;
  return str.replace(/\{(\w+)\}/g, (_, k) => String(vars[k] ?? `{${k}}`));
}
